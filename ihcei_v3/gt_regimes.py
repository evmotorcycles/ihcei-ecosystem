@dataclass
class RegimeCall:
    regime: Regime
    confidence: float          # 0–1, keyword-margin heuristic; override with `declared`
    rationale: str

    def to_dict(self) -> dict:
        d = asdict(self); d["regime"] = self.regime.value; return d


def classify_channel(role_description: str = "",
                     declared: Optional[Regime] = None) -> RegimeCall:
    """Gate 0. Classify a channel's ROLE before any law is applied.

    `declared` (an explicit institutional declaration of the channel's function)
    always wins over the keyword heuristic — the heuristic exists to catch the
    case where nobody asked the question at all, which is exactly how the
    judicial campaign went wrong."""
    if declared is not None:
        return RegimeCall(declared, 1.0, "explicitly declared by the architect")
    text = (role_description or "").lower()
    scores = {r: sum(1 for kw in kws if kw in text) for r, kws in _RUBRIC.items()}
    best = max(scores, key=scores.get)
    top, rest = scores[best], sorted(scores.values())[-2] if len(scores) > 1 else 0
    if top == 0:
        return RegimeCall(Regime.UNKNOWN, 0.0,
                          "no regime keywords found — declare the channel's role "
                          "explicitly; refusing to guess is the point of Gate 0")
    conf = min(1.0, 0.4 + 0.2 * (top - rest))
    hits = [kw for kw in _RUBRIC[best] if kw in text][:4]
    return RegimeCall(best, round(conf, 2), f"keyword evidence: {hits}")


