def correction_signature_check(median_E_by_class: dict) -> dict:
    """P2 helper — encodes the BGer five-level ordering as a reusable test.

    Expects keys among: writeoff, inadmissible, rejected, granted, partly_granted.
    PASS iff non-engagement < inspection-only < repair-delivered."""
    g = median_E_by_class.get
    need = ("rejected",)
    if any(k not in median_E_by_class for k in need):
        return {"pass": None, "why": "need at least 'rejected' plus one other class"}
    non_engage = [v for k, v in median_E_by_class.items()
                  if k in ("writeoff", "inadmissible")]
    repaired = [v for k, v in median_E_by_class.items()
                if k in ("granted", "partly_granted")]
    ok = True; notes = []
    if non_engage:
        ok &= max(non_engage) < g("rejected"); notes.append("non-engagement < inspection")
    if repaired:
        ok &= min(repaired) > g("rejected"); notes.append("repair > inspection")
    return {"pass": bool(ok), "why": "; ".join(notes),
            "open_detail_P4": "full vs partial ordering unresolved (BGer: partly ≈ granted)"}


