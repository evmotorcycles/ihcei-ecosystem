class RegimeError(ValueError):
    """Raised when a law is requested for a channel it does not govern."""


@dataclass
class PredictedE:
    value: float
    law: str
    status: str
    regime: str
    note: str

    def to_dict(self) -> dict:
        return asdict(self)


def predict_E(U: float,
              regime: Regime,
              D: Optional[float] = None,
              D_in: Optional[float] = None,
              q: Optional[float] = None,
              kappa: float = 1.0) -> PredictedE:
    """Predict realized output under the regime's own law.

    TRANSMISSION: requires D (the channel's own two-hop fidelity).
    CORRECTION : requires D_in (fidelity of the INCOMING signal) and q (the
                 corrector's own decode quality). Result is HYPOTHESIS-tagged.
    SELECTION / UNKNOWN: refused, with the reason."""
    if regime == Regime.TRANSMISSION:
        if D is None:
            raise RegimeError("transmission law needs D (own two-hop fidelity)")
        return PredictedE(float(U * D), "E = U*D", LawStatus.SUPPORTED.value,
                          regime.value, "engine law; linear, multi-domain supported")
    if regime == Regime.CORRECTION:
        if D_in is None or q is None:
            raise RegimeError("correction law needs D_in (incoming fidelity) and q "
                              "(corrector's own decode quality)")
        return PredictedE(float(U * kappa * (1.0 - D_in) * q),
                          "E_c = U*kappa*(1-D_in)*q", LawStatus.HYPOTHESIS.value,
                          regime.value,
                          "PUMP LAW — HYPOTHESIS. Post-hoc from BGer; do not report "
                          "as validated until P1–P2 (see preregistered_predictions) "
                          "pass out-of-sample.")
    if regime == Regime.SELECTION:
        raise RegimeError("no validated D-law for SELECTION channels (legislative "
                          "null: power covariates dominate). Model with the "
                          "channel's own covariates; do not launder an exponent.")
    raise RegimeError("regime UNKNOWN — run classify_channel / Gate 0 first; "
                      "refusing to apply a law to an unclassified channel is "
                      "the lesson of the judicial campaign.")


