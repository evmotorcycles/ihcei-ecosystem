    def _score_governance(self, text: str) -> float:
        """
        D_enc v2.0: hybrid governance fidelity scorer.

        Three-component blend:
          1. Keyword density (governance + OQM vocabulary)
          2. TF-IDF cosine against enriched governance corpus
          3. OQM methodology bonus — Al-Asr pressing signals raise D
             (transparency of process, not institutional credentials)

        Key v2.0 change: OQM methodology signals are rewarded independently
        of whether the text cites any human authority. A text that documents
        its extraction process and makes falsifiable claims scores high D
        regardless of whether scholars agree with it.
        """
        if not text.strip():
            return 0.0

        words_lower = set(re.findall(r'\b\w+\b', text.lower()))
        bigrams = set(
            w1 + " " + w2
            for w1, w2 in zip(text.lower().split(), text.lower().split()[1:])
        )
        all_tokens = words_lower | bigrams
        word_count = max(len(text.split()), 1)

        gov_hits = len(all_tokens & GOVERNANCE_KEYWORDS)
        ext_hits = len(all_tokens & EXTRACTION_KEYWORDS)
        oqm_hits = len(all_tokens & OQM_METHODOLOGY_KEYWORDS)

        gov_density = float(np.clip(gov_hits / (word_count / 10.0), 0.0, 1.0))
        ext_density = float(np.clip(ext_hits / (word_count / 10.0), 0.0, 1.0))
        oqm_density = float(np.clip(oqm_hits / (word_count / 15.0), 0.0, 1.0))

        keyword_D = float(np.clip(
            (gov_density + oqm_density * 0.5) * (1.0 - 0.5 * ext_density),
            0.0, 1.0
        ))

        try:
            all_texts = GOVERNANCE_CORPUS + EXTRACTION_CORPUS + [text]
            vec = TfidfVectorizer(max_features=1500, stop_words="english",
                                  ngram_range=(1, 2), min_df=1)
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                tfidf = vec.fit_transform(all_texts)
            gov_mean = np.asarray(tfidf[:len(GOVERNANCE_CORPUS)].mean(axis=0))
            text_vec = np.asarray(tfidf[-1].todense())
            cosine_D = float(np.clip(
                float(sk_cosine(text_vec, gov_mean)[0, 0]) * 4.0,
                0.0, 1.0
            ))
        except Exception:
            cosine_D = 0.3

        return float(np.clip(0.55 * keyword_D + 0.35 * cosine_D + 0.10 * oqm_density,
                             0.0, 1.0))

