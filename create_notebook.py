                "class Agent2008(Agent):\n",
                "    def __init__(self, agent_id, agent_type, u_init, d_init, leverage, mbs_exposure):\n",
                "        super().__init__(agent_id, agent_type, u_init, d_init)\n",
                "        self.leverage = leverage\n",
                "        self.mbs_exposure = mbs_exposure\n",
                "        self.defaulted = False\n",
                "        \n",
                "        # Initial 'Open Gates' calculation based on risk\n",
                "        # High leverage (>15) opens a gate. High MBS (>0.3) opens a gate.\n",
                "        self.update_gates()\n",
                "\n",
                "    def update_gates(self):\n",
                "        gates = 0\n",
                "        if self.leverage > 15: gates += 1\n",
                "        if self.leverage > 30: gates += 1\n",
                "        if self.mbs_exposure > 0.3: gates += 1\n",
                "        if self.mbs_exposure > 0.6: gates += 1\n",
                "        if self.defaulted: gates = 7 # Total corruption/collapse\n",
                "        self.open_gates = gates\n",
                "\n",
                "    def update_financials(self, hpi_change, delinquency_rate, policy_leverage_cap, policy_wealth_tax, policy_capital_req):\n",
                "        if self.defaulted:\n",
                "            self.u = 0\n",
                "            self.d = 0\n",
                "            self.calculate_state()\n",
                "            return\n",
                "\n",
                "        # 1. Apply Wealth Tax (reduces U)\n",
                "        self.u *= (1.0 - policy_wealth_tax)\n",
                "\n",
                "        # 2. Check Leverage Cap & Capital Requirements\n",
                "        # Capital Req % means Leverage <= 1 / Capital_Req\n",
                "        # e.g. 10% req -> max 10x leverage\n",
                "        eff_leverage_cap = policy_leverage_cap\n",
                "        if policy_capital_req > 0.01:\n",
                "             cap_based_limit = 1.0 / policy_capital_req\n",
                "             eff_leverage_cap = min(policy_leverage_cap, cap_based_limit)\n",
                "\n",
                "        if self.leverage > eff_leverage_cap:\n",
                "            # Forced deleveraging: sell assets, lose value\n",
                "            excess = self.leverage - eff_leverage_cap\n",
                "            self.u *= (1.0 - (excess * 0.02)) # Cost of selling (distressed sale)\n",
                "            self.leverage = eff_leverage_cap\n",
                "\n",
                "        # 3. Calculate MBS Losses\n",
                "        # Loss is proportional to HPI drop and delinquency\n",
                "        # If HPI is positive, gain small profit. If negative, huge loss leveraged.\n",
                "        \n",
                "        market_impact = hpi_change - (delinquency_rate * 0.5)\n",
                "        \n",
                "        # Leverage Multiplier effect\n",
                "        pnl = self.u * self.mbs_exposure * market_impact * self.leverage\n",
                "        \n",
                "        self.u += pnl\n",
                "        \n",
                "        # 4. Update Governance (D)\n",
                "        # Losing money creates panic -> lowers D\n",
                "        # High leverage inherently stresses D\n",
                "        if pnl < 0:\n",
                "            # Panic factor\n",
                "            self.d -= 0.05 * (abs(pnl) / (self.u + 1.0)) # Avoid div/0\n",
                "        \n",
                "        # 5. Check Default\n",
                "        if self.u <= 0:\n",
                "            self.u = 0\n",
                "            self.defaulted = True\n",
                "            self.d = 0\n",
                "            \n",
                "        # Clamp D\n",
                "        self.d = max(0.0, min(1.0, self.d))\n",
                "        \n",
                "        self.update_gates()\n",
                "        self.calculate_state()"
