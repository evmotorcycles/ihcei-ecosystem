                "class Agent:\n",
                "    def __init__(self, agent_id, agent_type, u_init, d_init):\n",
                "        self.id = agent_id\n",
                "        self.type = agent_type  # 'Materialist', 'Mixed', 'Sovereign'\n",
                "        self.u = u_init\n",
                "        self.d = d_init\n",
                "        self.essence = 0.0\n",
                "        self.friction = 0.0\n",
                "        self.open_gates = 0  # Number of vulnerabilities (0-7)\n",
                "        self.calculate_state()\n",
                "\n",
                "    def calculate_state(self):\n",
                "        \"\"\"Calculates Essence based on the Destiny Equation: E = U * D^2\"\"\"\n",
                "        # D cannot be negative for this calculation, though it can drop to 0\n",
                "        d_eff = max(0.0, self.d)\n",
                "        self.essence = self.u * (d_eff ** 2)\n",
                "        \n",
                "        # Friction increases with U (mass) and Open Gates\n",
                "        # Entropy Friction = U * (1 + 0.5 * Open_Gates)\n",
                "        self.friction = self.u * (1.0 + 0.5 * self.open_gates)\n",
                "\n",
                "class Network:\n",
                "    def __init__(self, agents):\n",
                "        self.agents = agents\n",
                "        self.num_agents = len(agents)\n",
                "        self.g_matrix = np.zeros((self.num_agents, self.num_agents))\n",
                "        self.c_dev = 0.0\n",
                "        self.total_entropy = 0.0\n",
                "        self.gini = 0.0\n",
                "        \n",
                "    def update_connectivity(self):\n",
                "        \"\"\"\n",
                "        Updates G_ij (Alignment Matrix).\n",
                "        In this model, connectivity is based on 'Trust' which is a function of D (Governance).\n",
                "        High D agents trust each other. Low D agents are isolated when panic hits.\n",
                "        \"\"\"\n",
                "        for i in range(self.num_agents):\n",
                "            for j in range(self.num_agents):\n",
                "                if i == j:\n",
                "                    self.g_matrix[i, j] = 1.0\n",
                "                    continue\n",
                "                \n",
                "                agent_i = self.agents[i]\n",
                "                agent_j = self.agents[j]\n",
                "                \n",
                "                # Trust is the product of their Governance scores\n",
                "                trust = agent_i.d * agent_j.d\n",
                "                self.g_matrix[i, j] = trust\n",
