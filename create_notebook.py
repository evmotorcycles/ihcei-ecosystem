                "        self.total_entropy = sum(a.friction for a in self.agents)\n",
                "        \n",
                "        # C_dev: Civilization Development Rate\n",
                "        # In a perfect system, it's the sum of Essence.\n",
                "        # In a corrupted system, it's Essence / Entropy_Resistance\n",
                "        # We normalize by N for scale.\n",
                "        if self.total_entropy > 0:\n",
                "             # We treat 'total_entropy' as the resistance factor\n",
                "             # Higher entropy -> Lower C_dev\n",
                "             # Base resistance is 1.0 per agent\n",
                "             base_resistance = self.num_agents * 1.0\n",
                "             resistance = max(1.0, self.total_entropy / base_resistance)\n",
                "             self.c_dev = total_essence / resistance\n",
                "        else:\n",
                "             self.c_dev = total_essence\n",
                "             \n",
                "        # Gini Coefficient of Utility (Wealth inequality)\n",
                "        u_values = sorted([a.u for a in self.agents])\n",
                "        n = len(u_values)\n",
                "        if sum(u_values) == 0:\n",
                "            self.gini = 0.0\n",
                "        else:\n",
                "            numer = 2 * sum((i + 1) * u for i, u in enumerate(u_values))\n",
                "            denom = n * sum(u_values)\n",
                "            self.gini = (numer / denom) - (n + 1) / n\n"
